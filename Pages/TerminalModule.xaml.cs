/// <summary>
/// Windows 終端機模組 · Windows Terminal module — three tabs:
///   1. Profiles editor: read / add / edit / delete / duplicate profiles in settings.json natively
///      (System.Text.Json DOM so unknown keys survive), set the default, pick paths via FileDialogs,
///      safe save (backup + atomic write).
