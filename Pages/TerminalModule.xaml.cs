    /// <summary>列表項目視圖模型 · A profile row bound to the ListView.</summary>
    public sealed class ProfileRow : System.ComponentModel.INotifyPropertyChanged
    {
        public WtProfile Profile { get; }
        public bool IsDefault { get; private set; }

        public ProfileRow(WtProfile p, bool isDefault) { Profile = p; IsDefault = isDefault; }

        public string DisplayName => string.IsNullOrWhiteSpace(Profile.Name) ? "(unnamed)" : Profile.Name;

        public string SubText
        {
            get
            {
                var bits = new List<string>();
                if (Profile.Hidden) bits.Add("hidden · 隱藏");
                if (Profile.IsGenerated) bits.Add(Profile.Source);
                var cmd = Profile.CommandLine;
                if (!string.IsNullOrEmpty(cmd)) bits.Add(cmd);
                return string.Join("  ·  ", bits);
            }
        }

        public Visibility DefaultStar => IsDefault ? Visibility.Visible : Visibility.Collapsed;

        public void SetDefault(bool v) { IsDefault = v; Raise(nameof(DefaultStar)); }
        public void Refresh() { Raise(nameof(DisplayName)); Raise(nameof(SubText)); }

        public event System.ComponentModel.PropertyChangedEventHandler? PropertyChanged;
        private void Raise(string n) => PropertyChanged?.Invoke(this, new System.ComponentModel.PropertyChangedEventArgs(n));
    }
