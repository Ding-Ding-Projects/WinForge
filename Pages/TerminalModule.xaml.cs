    private void LoadFields(ProfileRow row)
    {
        _loadingFields = true;
        var p = row.Profile;
        NameBox.Text = p.Name;
        CmdBox.Text = p.CommandLine;
        DirBox.Text = p.StartingDirectory;
        IconBox.Text = p.Icon;
        FontBox.Text = p.FontFace;
        HiddenToggle.IsOn = p.Hidden;

        // Scheme dropdown: built-ins + any defined in the file + current value.
        var schemes = new List<string> { "" };
        schemes.AddRange(WindowsTerminalService.BuiltInSchemes);
        if (_root is not null) schemes.AddRange(WindowsTerminalService.SchemeNames(_root));
        if (!string.IsNullOrEmpty(p.ColorScheme) && !schemes.Contains(p.ColorScheme)) schemes.Add(p.ColorScheme);
        SchemeBox.ItemsSource = schemes.Distinct().ToList();
        SchemeBox.SelectedItem = string.IsNullOrEmpty(p.ColorScheme) ? "" : p.ColorScheme;

        GeneratedNote.Visibility = p.IsGenerated ? Visibility.Visible : Visibility.Collapsed;
        SetDefaultBtn.IsEnabled = !row.IsDefault;
        _loadingFields = false;
    }

    private void Field_Changed(object sender, object e)
    {
        if (_loadingFields) return;
        var row = Selected;
        if (row is null) return;
        var p = row.Profile;
        p.Name = NameBox.Text;
        p.CommandLine = CmdBox.Text;
        p.StartingDirectory = DirBox.Text;
        p.Icon = IconBox.Text;
        p.FontFace = FontBox.Text;
        p.ColorScheme = SchemeBox.SelectedItem as string ?? "";
        p.Hidden = HiddenToggle.IsOn;
        row.Refresh();
        _dirty = true;
    }

    private void Add_Click(object sender, RoutedEventArgs e)
    {
        if (_root is null)
        {
            // No settings.json — offer to create a minimal one.
            if (_settingsPath is null)
                _settingsPath = WindowsTerminalService.CandidatePaths()[0];
            _root = new JsonObject
            {
                ["$schema"] = "https://aka.ms/terminal-profiles-schema",
                ["profiles"] = new JsonObject { ["defaults"] = new JsonObject(), ["list"] = new JsonArray() },
                ["schemes"] = new JsonArray(),
                ["actions"] = new JsonArray(),
            };
            PathText.Text = DisplayPath(_settingsPath);
        }

        var prof = WindowsTerminalService.NewProfile(P("New profile", "新設定檔"),
            "%SystemRoot%\\System32\\cmd.exe");
        WindowsTerminalService.ProfileListArray(_root).Add(prof.Node);
        if (string.IsNullOrEmpty(WindowsTerminalService.DefaultProfile(_root)))
            WindowsTerminalService.SetDefaultProfile(_root, prof.Guid);

        var row = new ProfileRow(prof, false);
        _rows.Add(row);
        ProfileList.SelectedItem = row;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Added", "已新增"), P("New profile created — edit it then Save.", "已建立新設定檔 — 改完撳儲存。"));
    }

    private void Duplicate_Click(object sender, RoutedEventArgs e)
    {
