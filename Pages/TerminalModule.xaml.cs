    private async void LaunchProfile_Click(object sender, RoutedEventArgs e)
    {
        var row = Selected;
        if (row is null) return;
        if (!WindowsTerminalService.WtAvailable())
        {
            Info(InfoBarSeverity.Warning, P("wt.exe not found", "搵唔到 wt.exe"), P("Install Windows Terminal first.", "請先安裝 Windows 終端機。"));
            return;
        }
        var args = WindowsTerminalService.BuildLaunchArgs(row.Profile.Name, row.Profile.StartingDirectory, newTab: true);
        var r = await ShellRunner.Run("wt.exe", args);
        Info(r.Success ? InfoBarSeverity.Success : InfoBarSeverity.Error,
            r.Success ? P("Launched", "已啟動") : P("Failed", "失敗"), $"wt {args}");
    }

    // ===================== Embedded terminal tab =====================

    private void BuildShellList()
    {
        var shells = new List<ShellOption>
        {
            new("Windows PowerShell", "powershell.exe"),
            new("Command Prompt · 命令提示字元", "cmd.exe"),
        };
        // pwsh (PowerShell 7) if present.
        var pwsh = ResolveOnPath("pwsh.exe");
        if (pwsh is not null) shells.Insert(0, new("PowerShell 7 (pwsh)", pwsh));
        // WSL default distro.
        if (ResolveOnPath("wsl.exe") is not null) shells.Add(new("WSL · Linux 子系統", "wsl.exe"));

        ShellBox.ItemsSource = shells;
        ShellBox.DisplayMemberPath = "Label";
        ShellBox.SelectedIndex = 0;
    }

    private static string? ResolveOnPath(string exe)
    {
        var pathVar = Environment.GetEnvironmentVariable("PATH") ?? "";
        foreach (var dir in pathVar.Split(Path.PathSeparator))
        {
            try
            {
                if (string.IsNullOrWhiteSpace(dir)) continue;
                var c = Path.Combine(dir.Trim(), exe);
                if (File.Exists(c)) return c;
            }
            catch { }
        }
        // System32 fallback for wsl.exe / powershell.exe.
        var sys = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.System), exe);
        return File.Exists(sys) ? sys : null;
    }

    private void StartTerm_Click(object sender, RoutedEventArgs e)
    {
        if (_pty is not null) StopTerminal();
        if (ShellBox.SelectedItem is not ShellOption opt) return;

        Term.ClearScreen();
        try
        {
            _pty = new ConPtySession();
            _pty.OutputReceived += s => Term.Feed(s);
            _pty.Exited += code => DispatcherQueue.TryEnqueue(() =>
            {
                Term.Feed($"\r\n[{P("process exited", "程序結束")}: {code}]\r\n");
                StopTerminal();
            });
            _pty.Start(opt.CommandLine, (short)Term.Columns, (short)Term.Rows,
                Environment.GetFolderPath(Environment.SpecialFolder.UserProfile));
            StartTermBtn.IsEnabled = false;
            StopTermBtn.IsEnabled = true;
            Term.Focus(FocusState.Programmatic);
            RefreshTermStatus();
        }
        catch (Exception ex)
        {
            _pty = null;
            Info(InfoBarSeverity.Error, P("Could not start shell", "開唔到 shell"), ex.Message);
            RefreshTermStatus();
        }
    }

    private void StopTerm_Click(object sender, RoutedEventArgs e) => StopTerminal();

    private void StopTerminal()
    {
        try { _pty?.Dispose(); } catch { }
        _pty = null;
        if (StartTermBtn is not null) StartTermBtn.IsEnabled = true;
        if (StopTermBtn is not null) StopTermBtn.IsEnabled = false;
        RefreshTermStatus();
    }

    private void ClearTerm_Click(object sender, RoutedEventArgs e) => Term.ClearScreen();

    private void RefreshTermStatus() => RefreshTermStatusCore();

    private void RefreshTermStatusCore()
    {
        if (TermStatus is null) return;
        TermStatus.Text = _pty is { IsRunning: true }
            ? P("Running — click the terminal and type. Ctrl+C interrupts.", "執行中 — 撳一下終端機就可以打字。Ctrl+C 中斷。")
            : P("Stopped.", "已停止。");
    }

