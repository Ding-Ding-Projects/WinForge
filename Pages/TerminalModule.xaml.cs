    private void ReloadProfiles()
    {
        _rows.Clear();
        ProfileList.ItemsSource = _rows;
        _settingsPath = WindowsTerminalService.Resolve();
        _dirty = false;

        if (_settingsPath is null)
        {
            PathText.Text = P("No settings.json found.", "搵唔到 settings.json。");
            _root = null;
            FieldsPanel.Visibility = Visibility.Collapsed;
            EditorHint.Text = P("Windows Terminal is not installed, so there is no settings.json to edit. Install it above, or use the embedded terminal tab.",
                "未安裝 Windows 終端機，所以冇 settings.json 可以改。喺上面安裝，或者用內嵌終端機分頁。");
            return;
        }

        try
        {
            _root = WindowsTerminalService.Load(_settingsPath);
            PathText.Text = DisplayPath(_settingsPath);
            PopulateList();
            Info(InfoBarSeverity.Informational, P("Loaded", "已載入"),
                $"{_rows.Count} " + P("profiles", "個設定檔"));
        }
        catch (Exception ex)
        {
            _root = null;
            Info(InfoBarSeverity.Error, P("Failed to read settings.json", "讀 settings.json 失敗"), ex.Message);
        }
    }

    private void PopulateList()
    {
        _rows.Clear();
        if (_root is null) return;
        var def = WindowsTerminalService.DefaultProfile(_root);
        foreach (var p in WindowsTerminalService.Profiles(_root))
            _rows.Add(new ProfileRow(p, p.Guid == def));
    }

    private void RefreshDefaultStars()
    {
        if (_root is null) return;
        var def = WindowsTerminalService.DefaultProfile(_root);
        foreach (var r in _rows) r.SetDefault(r.Profile.Guid == def);
    }

