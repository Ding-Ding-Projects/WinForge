        catch (UnauthorizedAccessException)
        {
            Info(InfoBarSeverity.Error, P("Save failed", "儲存失敗"), P("Access denied writing settings.json.", "寫入 settings.json 被拒。"));
        }
        catch (Exception ex)
        {
            Info(InfoBarSeverity.Error, P("Save failed", "儲存失敗"), ex.Message);
        }
    }

