    private async void Delete_Click(object sender, RoutedEventArgs e)
    {
        var row = Selected;
        if (row is null || _root is null) return;

        var dlg = new ContentDialog
        {
            XamlRoot = XamlRoot,
            Title = P("Delete profile?", "刪除設定檔？"),
            Content = P($"Remove \"{row.Profile.Name}\" from settings.json? This is applied when you Save.",
                $"由 settings.json 移除「{row.Profile.Name}」？撳儲存先生效。"),
            PrimaryButtonText = P("Delete", "刪除"),
            CloseButtonText = P("Cancel", "取消"),
            DefaultButton = ContentDialogButton.Close,
        };
        if (await dlg.ShowAsync() != ContentDialogResult.Primary) return;

        var arr = WindowsTerminalService.ProfileListArray(_root);
        for (int i = 0; i < arr.Count; i++)
        {
            if (arr[i] is JsonObject o && ReferenceEquals(o, row.Profile.Node))
            {
                arr.RemoveAt(i);
                break;
            }
        }
        _rows.Remove(row);
        FieldsPanel.Visibility = Visibility.Collapsed;
        EditorHint.Visibility = Visibility.Visible;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Removed", "已移除"), P("Click Save to write the change.", "撳儲存先寫入更改。"));
    }

