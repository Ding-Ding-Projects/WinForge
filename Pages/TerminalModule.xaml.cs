    private void Save_Click(object sender, RoutedEventArgs e)
    {
        if (_root is null || _settingsPath is null)
        {
            Info(InfoBarSeverity.Warning, P("Nothing to save", "冇嘢可以儲存"), P("There is no settings.json loaded.", "未載入任何 settings.json。"));
            return;
        }
        try
        {
            var backup = WindowsTerminalService.Save(_settingsPath, _root);
            _dirty = false;
            var msg = string.IsNullOrEmpty(backup)
                ? P("Saved (new file).", "已儲存（新檔）。")
