    private void UpdateInstallBar()
    {
        bool wt = WindowsTerminalService.WtAvailable();
        bool settings = WindowsTerminalService.SettingsExist();

        InstallBar.ActionButton = null;
        if (wt || settings)
        {
            InstallBar.IsOpen = false;
            return;
        }

        InstallBar.Severity = Microsoft.UI.Xaml.Controls.InfoBarSeverity.Warning;
        InstallBar.Title = P("Windows Terminal not found", "搵唔到 Windows 終端機");
        InstallBar.Message = P("The profiles editor and wt.exe launch need Windows Terminal. The embedded ConPTY terminal works without it.",
            "設定檔編輯器同 wt.exe 啟動需要 Windows 終端機。內嵌 ConPTY 終端機冇佢都用得。");
        InstallBar.ActionButton = EngineBars.AutoInstallButton(WingetId,
            "Install Windows Terminal", "安裝 Windows 終端機",
            async () => { await System.Threading.Tasks.Task.Yield(); UpdateInstallBar(); ReloadProfiles(); },
            () => WindowsTerminalService.Resolve());
        InstallBar.IsOpen = true;
    }

    // ===================== Profiles tab =====================

    private void ReloadProfiles()
    {
        _rows.Clear();
        ProfileList.ItemsSource = _rows;
        _settingsPath = WindowsTerminalService.Resolve();
        _dirty = false;

        if (_settingsPath is null)
        {
            PathText.Text = P("No settings.json found.", "搵唔到 settings.json。");
            _root = null;
            FieldsPanel.Visibility = Visibility.Collapsed;
            EditorHint.Text = P("Windows Terminal is not installed, so there is no settings.json to edit. Install it above, or use the embedded terminal tab.",
                "未安裝 Windows 終端機，所以冇 settings.json 可以改。喺上面安裝，或者用內嵌終端機分頁。");
            return;
        }

        try
        {
            _root = WindowsTerminalService.Load(_settingsPath);
            PathText.Text = DisplayPath(_settingsPath);
            PopulateList();
            Info(InfoBarSeverity.Informational, P("Loaded", "已載入"),
                $"{_rows.Count} " + P("profiles", "個設定檔"));
        }
        catch (Exception ex)
        {
            _root = null;
            Info(InfoBarSeverity.Error, P("Failed to read settings.json", "讀 settings.json 失敗"), ex.Message);
        }
    }

    private void PopulateList()
    {
        _rows.Clear();
        if (_root is null) return;
        var def = WindowsTerminalService.DefaultProfile(_root);
        foreach (var p in WindowsTerminalService.Profiles(_root))
            _rows.Add(new ProfileRow(p, p.Guid == def));
    }

    private void RefreshDefaultStars()
    {
        if (_root is null) return;
        var def = WindowsTerminalService.DefaultProfile(_root);
        foreach (var r in _rows) r.SetDefault(r.Profile.Guid == def);
    }

    private ProfileRow? Selected => ProfileList.SelectedItem as ProfileRow;

    private void Reload_Click(object sender, RoutedEventArgs e)
    {
        if (_dirty)
        {
            // Soft warn but reload (user explicitly clicked).
        }
        ReloadProfiles();
    }

    private void ProfileList_SelectionChanged(object sender, SelectionChangedEventArgs e)
    {
        var row = Selected;
        if (row is null) { FieldsPanel.Visibility = Visibility.Collapsed; EditorHint.Visibility = Visibility.Visible; return; }
        EditorHint.Visibility = Visibility.Collapsed;
        FieldsPanel.Visibility = Visibility.Visible;
        LoadFields(row);
    }

    private void LoadFields(ProfileRow row)
    {
        _loadingFields = true;
        var p = row.Profile;
        NameBox.Text = p.Name;
        CmdBox.Text = p.CommandLine;
        DirBox.Text = p.StartingDirectory;
        IconBox.Text = p.Icon;
        FontBox.Text = p.FontFace;
        HiddenToggle.IsOn = p.Hidden;

        // Scheme dropdown: built-ins + any defined in the file + current value.
        var schemes = new List<string> { "" };
        schemes.AddRange(WindowsTerminalService.BuiltInSchemes);
        if (_root is not null) schemes.AddRange(WindowsTerminalService.SchemeNames(_root));
        if (!string.IsNullOrEmpty(p.ColorScheme) && !schemes.Contains(p.ColorScheme)) schemes.Add(p.ColorScheme);
        SchemeBox.ItemsSource = schemes.Distinct().ToList();
        SchemeBox.SelectedItem = string.IsNullOrEmpty(p.ColorScheme) ? "" : p.ColorScheme;

        GeneratedNote.Visibility = p.IsGenerated ? Visibility.Visible : Visibility.Collapsed;
        SetDefaultBtn.IsEnabled = !row.IsDefault;
        _loadingFields = false;
    }

    private void Field_Changed(object sender, object e)
    {
        if (_loadingFields) return;
        var row = Selected;
        if (row is null) return;
        var p = row.Profile;
        p.Name = NameBox.Text;
        p.CommandLine = CmdBox.Text;
        p.StartingDirectory = DirBox.Text;
        p.Icon = IconBox.Text;
        p.FontFace = FontBox.Text;
        p.ColorScheme = SchemeBox.SelectedItem as string ?? "";
        p.Hidden = HiddenToggle.IsOn;
        row.Refresh();
        _dirty = true;
    }

    private void Add_Click(object sender, RoutedEventArgs e)
    {
        if (_root is null)
        {
            // No settings.json — offer to create a minimal one.
            if (_settingsPath is null)
                _settingsPath = WindowsTerminalService.CandidatePaths()[0];
            _root = new JsonObject
            {
                ["$schema"] = "https://aka.ms/terminal-profiles-schema",
                ["profiles"] = new JsonObject { ["defaults"] = new JsonObject(), ["list"] = new JsonArray() },
                ["schemes"] = new JsonArray(),
                ["actions"] = new JsonArray(),
            };
            PathText.Text = DisplayPath(_settingsPath);
        }

        var prof = WindowsTerminalService.NewProfile(P("New profile", "新設定檔"),
            "%SystemRoot%\\System32\\cmd.exe");
        WindowsTerminalService.ProfileListArray(_root).Add(prof.Node);
        if (string.IsNullOrEmpty(WindowsTerminalService.DefaultProfile(_root)))
            WindowsTerminalService.SetDefaultProfile(_root, prof.Guid);

        var row = new ProfileRow(prof, false);
        _rows.Add(row);
        ProfileList.SelectedItem = row;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Added", "已新增"), P("New profile created — edit it then Save.", "已建立新設定檔 — 改完撳儲存。"));
    }

    private void Duplicate_Click(object sender, RoutedEventArgs e)
    {
        var row = Selected;
        if (row is null || _root is null) return;
        var clone = WindowsTerminalService.Duplicate(row.Profile);
        WindowsTerminalService.ProfileListArray(_root).Add(clone.Node);
        var newRow = new ProfileRow(clone, false);
        _rows.Add(newRow);
        ProfileList.SelectedItem = newRow;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Duplicated", "已複製"), clone.Name);
    }

    private async void Delete_Click(object sender, RoutedEventArgs e)
    {
        var row = Selected;
        if (row is null || _root is null) return;

        var dlg = new ContentDialog
        {
            XamlRoot = XamlRoot,
            Title = P("Delete profile?", "刪除設定檔？"),
            Content = P($"Remove \"{row.Profile.Name}\" from settings.json? This is applied when you Save.",
                $"由 settings.json 移除「{row.Profile.Name}」？撳儲存先生效。"),
            PrimaryButtonText = P("Delete", "刪除"),
            CloseButtonText = P("Cancel", "取消"),
            DefaultButton = ContentDialogButton.Close,
        };
        if (await dlg.ShowAsync() != ContentDialogResult.Primary) return;

        var arr = WindowsTerminalService.ProfileListArray(_root);
        for (int i = 0; i < arr.Count; i++)
        {
            if (arr[i] is JsonObject o && ReferenceEquals(o, row.Profile.Node))
            {
                arr.RemoveAt(i);
                break;
            }
        }
        _rows.Remove(row);
        FieldsPanel.Visibility = Visibility.Collapsed;
        EditorHint.Visibility = Visibility.Visible;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Removed", "已移除"), P("Click Save to write the change.", "撳儲存先寫入更改。"));
    }

    private void SetDefault_Click(object sender, RoutedEventArgs e)
    {
        var row = Selected;
        if (row is null || _root is null) return;
        WindowsTerminalService.SetDefaultProfile(_root, row.Profile.Guid);
        RefreshDefaultStars();
        SetDefaultBtn.IsEnabled = false;
        _dirty = true;
        Info(InfoBarSeverity.Informational, P("Default set", "已設為預設"), row.Profile.Name);
    }

    private void Save_Click(object sender, RoutedEventArgs e)
    {
        if (_root is null || _settingsPath is null)
        {
            Info(InfoBarSeverity.Warning, P("Nothing to save", "冇嘢可以儲存"), P("There is no settings.json loaded.", "未載入任何 settings.json。"));
            return;
        }
        try
        {
            var backup = WindowsTerminalService.Save(_settingsPath, _root);
            _dirty = false;
            var msg = string.IsNullOrEmpty(backup)
                ? P("Saved (new file).", "已儲存（新檔）。")
                : P($"Saved. Backup: {Path.GetFileName(backup)}", $"已儲存。備份：{Path.GetFileName(backup)}");
            Info(InfoBarSeverity.Success, P("Saved", "已儲存"), msg);
        }
        catch (UnauthorizedAccessException)
        {
            Info(InfoBarSeverity.Error, P("Save failed", "儲存失敗"), P("Access denied writing settings.json.", "寫入 settings.json 被拒。"));
        }
        catch (Exception ex)
        {
            Info(InfoBarSeverity.Error, P("Save failed", "儲存失敗"), ex.Message);
        }
    }

