    TextStats AnalyzeTextStats(
        std::wstring_view text,
        bool ignoreStopWords,
        int topN) noexcept
    {
        TextStats result;
        try
        {
            if (topN < 1) topN = 1;
            result.characters = static_cast<int>(text.size());
            for (auto const value : text)
            {
                if (!IsManagedWhitespace(static_cast<std::uint16_t>(value)))
                {
                    ++result.charactersNoSpaces;
                }
            }

            auto const words = TokenizeTextStats(text);
            result.words = static_cast<int>(words.size());
            result.sentences = CountSentences(text);
            if (result.sentences == 0 && result.words > 0) result.sentences = 1;
            result.paragraphs = CountParagraphs(text);

            int totalWordCharacters{};
            std::vector<CountedTerm> unique;
            std::vector<CountedTerm> frequency;
            std::unordered_map<std::wstring, std::size_t> uniqueIndices;
            std::unordered_map<std::wstring, std::size_t> frequencyIndices;
            unique.reserve(words.size());
            frequency.reserve(words.size());
            uniqueIndices.reserve(words.size());
            frequencyIndices.reserve(words.size());
            auto const* stopWords = ignoreStopWords ? &TextStatsStopWordSet() : nullptr;
            for (auto const& word : words)
            {
                totalWordCharacters += static_cast<int>(word.size());
                result.syllables += SyllablesIn(word);
                auto const folded = FoldOrdinalIgnoreCase(word);
                AddOrdinalIgnoreCaseCount(unique, uniqueIndices, word, folded);
                if (!stopWords || !stopWords->contains(folded))
                {
                    AddOrdinalIgnoreCaseCount(frequency, frequencyIndices, word, folded);
                }
            }
            result.uniqueWords = static_cast<int>(unique.size());
            result.avgWordLength = result.words > 0
                ? static_cast<double>(totalWordCharacters) / result.words : 0.0;
            result.avgSentenceLength = result.sentences > 0
                ? static_cast<double>(result.words) / result.sentences : 0.0;
            result.readingMinutes = static_cast<double>(result.words) / 200.0;
            result.speakingMinutes = static_cast<double>(result.words) / 130.0;

            if (result.words > 0 && result.sentences > 0)
            {
                auto const wordsPerSentence =
                    static_cast<double>(result.words) / result.sentences;
                auto const syllablesPerWord =
                    static_cast<double>(result.syllables) / result.words;
                auto ease = 206.835 - 1.015 * wordsPerSentence - 84.6 * syllablesPerWord;
                auto grade = 0.39 * wordsPerSentence + 11.8 * syllablesPerWord - 15.59;
                result.fleschReadingEase = RoundToEven((std::clamp)(ease, -100.0, 121.0), 1);
                result.fleschKincaidGrade = RoundToEven((std::max)(0.0, grade), 1);
            }

            std::stable_sort(frequency.begin(), frequency.end(),
                [](CountedTerm const& left, CountedTerm const& right)
                {
                    if (left.count != right.count) return left.count > right.count;
                    return CompareFoldedOrdinal(left.folded, right.folded) < 0;
                });
            auto const take = (std::min)(static_cast<std::size_t>(topN), frequency.size());
            result.topWords.reserve(take);
            for (std::size_t index{}; index < take; ++index)
            {
                result.topWords.push_back({ frequency[index].term, frequency[index].count });
            }
        }
        catch (...)
        {
            return {};
        }
        return result;
    }

    std::wstring FormatDuration(double minutes) noexcept
    {
        try
        {
            if (minutes <= 0.0) return L"0s";
            auto rounded = std::nearbyint(minutes * 60.0);
            int totalSeconds{};
            if (std::isnan(rounded))
            {
                totalSeconds = 0;
            }
            else if (rounded >= static_cast<double>((std::numeric_limits<int>::max)()))
            {
                totalSeconds = (std::numeric_limits<int>::max)();
            }
            else if (rounded <= static_cast<double>((std::numeric_limits<int>::min)()))
            {
                totalSeconds = (std::numeric_limits<int>::min)();
            }
            else
            {
                totalSeconds = static_cast<int>(rounded);
            }
            if (totalSeconds < 1) totalSeconds = 1;
            auto const minutesPart = totalSeconds / 60;
            auto const secondsPart = totalSeconds % 60;
            if (minutesPart <= 0) return std::to_wstring(secondsPart) + L"s";
            wchar_t seconds[3]{};
            std::swprintf(seconds, std::size(seconds), L"%02d", secondsPart);
            return std::to_wstring(minutesPart) + L"m " + seconds + L"s";
        }
        catch (...)
        {
            return L"0s";
        }
    }

    WordFrequencyResult AnalyzeWordFrequency(
        std::wstring_view text,
        WordFrequencyOptions const& options) noexcept
    {
        WordFrequencyResult result;
        try
        {
            if (!HasNonWhitespace(text)) return result;
            std::vector<std::wstring> tokens;
            if (options.mode == FrequencyMode::Characters)
            {
                tokens = FrequencyCharacters(text, options.caseInsensitive);
            }
            else
            {
                auto words = FrequencyWords(text, options);
                if (options.mode == FrequencyMode::Bigrams)
                {
                    if (words.size() > 1) tokens.reserve(words.size() - 1);
                    for (std::size_t index{}; index + 1 < words.size(); ++index)
                    {
                        tokens.push_back(words[index] + L" " + words[index + 1]);
                    }
                }
                else
                {
                    tokens = std::move(words);
                }
            }
            if (tokens.empty()) return result;

            std::vector<CountedTerm> counts;
            std::unordered_map<std::wstring, std::size_t> indices;
            counts.reserve(tokens.size());
            indices.reserve(tokens.size());
            for (auto const& token : tokens) AddOrdinalCount(counts, indices, token);
            result.totalTokens = static_cast<int>(tokens.size());
            result.uniqueTokens = static_cast<int>(counts.size());
            result.diversity = static_cast<double>(result.uniqueTokens) / result.totalTokens;
            std::stable_sort(counts.begin(), counts.end(),
                [](CountedTerm const& left, CountedTerm const& right)
                {
                    if (left.count != right.count) return left.count > right.count;
                    return CompareCurrentCultureIgnoreCase(left.term, right.term) < 0;
                });

            auto const maximum = counts.front().count;
            result.rows.reserve(counts.size());
            int rank = 1;
            for (auto& entry : counts)
            {
                auto const fraction = maximum > 0
                    ? static_cast<double>(entry.count) / maximum : 0.0;
                auto const percentage = result.totalTokens > 0
                    ? static_cast<double>(entry.count) * 100.0 / result.totalTokens : 0.0;
                result.rows.push_back({
                    rank++, std::move(entry.term), entry.count,
                    (std::max)(2.0, fraction * 220.0),
                    FormatOneDecimal(percentage) + L"%"
                });
            }
        }
        catch (...)
        {
            // The managed oracle returns whatever was accumulated.
        }
        return result;
    }

    std::wstring ToWordFrequencyCsv(WordFrequencyResult const& result) noexcept
    {
        std::wstring csv;
        try
        {
            csv = L"Rank,Term,Count,Percent\r\n";
            for (auto const& row : result.rows)
            {
                csv.append(std::to_wstring(row.rank));
                csv.push_back(L',');
                csv.append(EscapeCsv(row.term));
                csv.push_back(L',');
                csv.append(std::to_wstring(row.count));
                csv.push_back(L',');
                csv.append(row.percent);
                csv.push_back(L'\n');
            }
        }
        catch (...)
        {
            // The managed oracle returns whatever was accumulated.
        }
        return csv;
    }

    StringComparisonMetrics ComputeStringComparison(
        std::wstring_view a,
        std::wstring_view b,
        bool ignoreCase,
        bool ignoreWhitespace) noexcept
    {
        try
        {
            auto const normalizedA = NormalizeComparison(a, ignoreCase, ignoreWhitespace);
            auto const normalizedB = NormalizeComparison(b, ignoreCase, ignoreWhitespace);
            StringComparisonMetrics result;
            result.lenA = static_cast<int>(normalizedA.size());
            result.lenB = static_cast<int>(normalizedB.size());
            result.truncated = normalizedA.size() > MaxStringCompareLength ||
                normalizedB.size() > MaxStringCompareLength;
            if (result.truncated)
            {
                result.levenshtein = -1;
                result.similarityPct = (std::numeric_limits<double>::quiet_NaN)();
                result.damerau = -1;
                result.longestCommonSubstring = -1;
                result.longestCommonSubsequence = -1;
            }
            else
            {
                result.levenshtein = Levenshtein(normalizedA, normalizedB);
                auto const maximum = (std::max)(normalizedA.size(), normalizedB.size());
                result.similarityPct = maximum == 0 ? 100.0 :
                    (1.0 - static_cast<double>(result.levenshtein) / maximum) * 100.0;
                result.damerau = DamerauLevenshtein(normalizedA, normalizedB);
                result.longestCommonSubstring = LongestCommonSubstring(normalizedA, normalizedB);
                result.longestCommonSubsequence = LongestCommonSubsequence(normalizedA, normalizedB);
            }
            result.hamming = normalizedA.size() == normalizedB.size()
                ? Hamming(normalizedA, normalizedB) : -1;
            result.jaroWinkler = JaroWinkler(normalizedA, normalizedB);
            return result;
        }
        catch (...)
        {
            return { 0, 0, false, -1,
                (std::numeric_limits<double>::quiet_NaN)(), -1, -1,
                (std::numeric_limits<double>::quiet_NaN)(), -1, -1 };
        }
    }

    std::wstring BuildStringComparisonReport(
        std::vector<std::pair<std::wstring, std::wstring>> const& rows) noexcept
    {
        try
        {
            std::wstring report;
            for (auto const& [label, value] : rows)
            {
                report.append(label);
                report.append(L": ");
                report.append(value);
                report.append(L"\r\n");
            }
            while (!report.empty() &&
                IsManagedWhitespace(static_cast<std::uint16_t>(report.back())))
            {
                report.pop_back();
            }
            return report;
        }
        catch (...)
        {
            return {};
        }
    }
