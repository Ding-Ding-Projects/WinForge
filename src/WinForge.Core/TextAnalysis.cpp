    [[nodiscard]] bool HasNonWhitespace(std::wstring_view text) noexcept
    {
        for (auto const value : text)
        {
            if (!IsManagedWhitespace(static_cast<std::uint16_t>(value))) return true;
        }
        return false;
    }

    [[nodiscard]] std::vector<std::wstring> TokenizeTextStats(std::wstring_view text)
    {
        std::vector<std::wstring> words;
        std::wstring current;
        for (auto const value : text)
        {
            if (IsCjk(value))
            {
                if (!current.empty())
                {
                    words.push_back(std::move(current));
                    current.clear();
                }
                words.emplace_back(1, value);
            }
            else if (IsLetterOrDigit(value) || value == L'\'' || value == L'\u2019')
            {
                current.push_back(value);
            }
            else if (!current.empty())
            {
                words.push_back(std::move(current));
                current.clear();
            }
        }
        if (!current.empty()) words.push_back(std::move(current));
        return words;
    }

    [[nodiscard]] int CountSentences(std::wstring_view text) noexcept
    {
        int count{};
        bool inRun{};
        for (auto const value : text)
        {
            auto const terminal = value == L'.' || value == L'!' || value == L'?' ||
                value == L'\u3002' || value == L'\uFF01' || value == L'\uFF1F';
            if (terminal)
            {
                if (!inRun) { ++count; inRun = true; }
            }
            else if (!IsManagedWhitespace(static_cast<std::uint16_t>(value)))
            {
                inRun = false;
            }
        }
        return count;
    }

    [[nodiscard]] int CountParagraphs(std::wstring_view text)
    {
        std::wstring normalized;
        normalized.reserve(text.size());
        for (std::size_t index{}; index < text.size(); ++index)
        {
            if (text[index] == L'\r')
            {
                if (index + 1 < text.size() && text[index + 1] == L'\n') ++index;
                normalized.push_back(L'\n');
            }
            else
            {
                normalized.push_back(text[index]);
            }
        }

        int count{};
        std::size_t start{};
        while (true)
        {
            auto const delimiter = normalized.find(L"\n\n", start);
            auto const block = delimiter == std::wstring::npos
                ? std::wstring_view(normalized).substr(start)
                : std::wstring_view(normalized).substr(start, delimiter - start);
            if (HasNonWhitespace(block)) ++count;
            if (delimiter == std::wstring::npos) break;
            start = delimiter + 2;
        }
        return count;
    }

    [[nodiscard]] int SyllablesIn(std::wstring_view word)
    {
        if (word.empty()) return 0;
        if (word.size() == 1 && IsCjk(word.front())) return 1;
        auto const lowered = ToLowerInvariant(word);
        std::wstring letters;
        for (auto const value : lowered)
        {
            if (value >= L'a' && value <= L'z') letters.push_back(value);
        }
        if (letters.empty()) return 1;

        int count{};
        bool previousVowel{};
        for (auto const value : letters)
        {
            auto const vowel = value == L'a' || value == L'e' || value == L'i' ||
                value == L'o' || value == L'u' || value == L'y';
            if (vowel && !previousVowel) ++count;
            previousVowel = vowel;
        }
        if (letters.back() == L'e' && count > 1) --count;
        return (std::max)(1, count);
    }

    [[nodiscard]] double RoundToEven(double value, int digits) noexcept
    {
        static constexpr std::array<double, 2> powers{ 1.0, 10.0 };
        auto const scale = powers[static_cast<std::size_t>((std::clamp)(digits, 0, 1))];
        return std::nearbyint(value * scale) / scale;
    }

    struct CountedTerm
    {
        std::wstring term;
        std::wstring folded;
        int count{};
    };

    void AddOrdinalCount(
        std::vector<CountedTerm>& counts,
        std::unordered_map<std::wstring, std::size_t>& indices,
        std::wstring const& term)
    {
        auto const [found, inserted] = indices.try_emplace(term, counts.size());
        if (!inserted)
        {
            ++counts[found->second].count;
            return;
        }
        counts.push_back({ term, {}, 1 });
    }

    void AddOrdinalIgnoreCaseCount(
        std::vector<CountedTerm>& counts,
        std::unordered_map<std::wstring, std::size_t>& indices,
        std::wstring const& term,
        std::wstring const& folded)
    {
        auto const [found, inserted] = indices.try_emplace(folded, counts.size());
        if (!inserted)
        {
            ++counts[found->second].count;
            return;
        }
        counts.push_back({ term, folded, 1 });
    }

    [[nodiscard]] std::wstring TrimApostropheHyphen(std::wstring value)
    {
        auto first = std::size_t{};
        while (first < value.size() && (value[first] == L'\'' || value[first] == L'-')) ++first;
        auto last = value.size();
        while (last > first && (value[last - 1] == L'\'' || value[last - 1] == L'-')) --last;
        return value.substr(first, last - first);
    }

    [[nodiscard]] std::vector<std::wstring> TokenizeFrequency(
        std::wstring_view text,
        bool stripPunctuation)
    {
        std::vector<std::wstring> tokens;
        if (!stripPunctuation)
        {
            std::size_t start{};
            while (start < text.size())
            {
                while (start < text.size() &&
                    IsManagedWhitespace(static_cast<std::uint16_t>(text[start]))) ++start;
                auto end = start;
                while (end < text.size() &&
                    !IsManagedWhitespace(static_cast<std::uint16_t>(text[end]))) ++end;
                if (end > start) tokens.emplace_back(text.substr(start, end - start));
                start = end;
            }
            return tokens;
        }

        std::wstring current;
        for (auto const value : text)
        {
            if (IsLetterOrDigit(value) || value == L'\'' || value == L'-')
            {
                current.push_back(value);
            }
            else if (!current.empty())
            {
                tokens.push_back(TrimApostropheHyphen(std::move(current)));
                current.clear();
            }
        }
        if (!current.empty()) tokens.push_back(TrimApostropheHyphen(std::move(current)));
        return tokens;
    }

    [[nodiscard]] std::vector<std::wstring> FrequencyWords(
        std::wstring_view text,
        WordFrequencyOptions const& options)
    {
        std::vector<std::wstring> words;
        auto const minimum = (std::max)(1, options.minLength);
        auto const* stopWords = options.removeStopWords ? &FrequencyStopWordSet() : nullptr;
        for (auto raw : TokenizeFrequency(text, options.stripPunctuation))
        {
            if (options.caseInsensitive) raw = ToLowerInvariant(raw);
            if (static_cast<int>(raw.size()) < minimum) continue;
            if (stopWords)
            {
                auto const folded = FoldOrdinalIgnoreCase(raw);
                if (stopWords->contains(folded)) continue;
            }
            words.push_back(std::move(raw));
        }
        return words;
    }

    [[nodiscard]] std::vector<std::wstring> FrequencyCharacters(
        std::wstring_view text,
        bool caseInsensitive)
    {
        std::vector<std::wstring> characters;
        for (std::size_t index{}; index < text.size(); ++index)
        {
            auto value = static_cast<std::uint32_t>(text[index]);
            if constexpr (sizeof(wchar_t) == 2)
            {
                if (IsHighSurrogate(value) && index + 1 < text.size() &&
                    IsLowSurrogate(static_cast<std::uint32_t>(text[index + 1])))
                {
                    auto const low = static_cast<std::uint32_t>(text[++index]);
                    value = 0x10000u + ((value - 0xD800u) << 10) + (low - 0xDC00u);
                }
                else if (IsHighSurrogate(value) || IsLowSurrogate(value))
                {
                    value = 0xFFFDu;
                }
            }
            if (IsManagedWhitespace(value)) continue;
            if (caseInsensitive) value = LowerInvariant(value);
            std::wstring scalar;
            AppendScalar(scalar, value);
            characters.push_back(std::move(scalar));
        }
        return characters;
    }

    [[nodiscard]] std::wstring FormatOneDecimal(double value)
    {
        // The managed custom numeric format "0.0" rounds midpoint values away
        // from zero. Percentages are bounded to 0..100, so integer tenths are
        // both exact and independent of the process C locale.
        auto const tenths = static_cast<int>(std::round(value * 10.0));
        return std::to_wstring(tenths / 10) + L"." +
            std::to_wstring((std::abs)(tenths % 10));
    }

    [[nodiscard]] std::wstring EscapeCsv(std::wstring_view value)
    {
        std::wstring escaped;
        escaped.reserve(value.size() + 2);
        escaped.push_back(L'"');
        for (auto const ch : value)
        {
            if (ch == L'"') escaped.push_back(L'"');
            escaped.push_back(ch);
        }
        escaped.push_back(L'"');
        return escaped;
    }

    [[nodiscard]] std::wstring NormalizeComparison(
        std::wstring_view input,
        bool ignoreCase,
        bool ignoreWhitespace)
    {
        std::wstring normalized;
        normalized.reserve(input.size());
        for (auto const value : input)
        {
            if (!ignoreWhitespace ||
                !IsManagedWhitespace(static_cast<std::uint16_t>(value)))
            {
                normalized.push_back(value);
            }
        }
        return ignoreCase ? ToLowerInvariant(normalized) : normalized;
    }

    [[nodiscard]] int Levenshtein(std::wstring_view a, std::wstring_view b)
    {
        if (a.empty()) return static_cast<int>(b.size());
        if (b.empty()) return static_cast<int>(a.size());
        std::vector<int> previous(b.size() + 1);
        std::vector<int> current(b.size() + 1);
        for (std::size_t j{}; j <= b.size(); ++j) previous[j] = static_cast<int>(j);
        for (std::size_t i = 1; i <= a.size(); ++i)
        {
            current[0] = static_cast<int>(i);
            for (std::size_t j = 1; j <= b.size(); ++j)
            {
                auto const cost = a[i - 1] == b[j - 1] ? 0 : 1;
                current[j] = (std::min)({ current[j - 1] + 1,
                    previous[j] + 1, previous[j - 1] + cost });
            }
            std::swap(previous, current);
        }
        return previous[b.size()];
    }

    [[nodiscard]] int DamerauLevenshtein(std::wstring_view a, std::wstring_view b)
    {
        auto const la = a.size();
        auto const lb = b.size();
        if (la == 0) return static_cast<int>(lb);
        if (lb == 0) return static_cast<int>(la);
        std::vector<int> distances((la + 1) * (lb + 1));
        auto cell = [&](std::size_t i, std::size_t j) -> int&
        {
            return distances[i * (lb + 1) + j];
        };
        for (std::size_t i{}; i <= la; ++i) cell(i, 0) = static_cast<int>(i);
        for (std::size_t j{}; j <= lb; ++j) cell(0, j) = static_cast<int>(j);
        for (std::size_t i = 1; i <= la; ++i)
        {
            for (std::size_t j = 1; j <= lb; ++j)
            {
                auto const cost = a[i - 1] == b[j - 1] ? 0 : 1;
                auto value = (std::min)({ cell(i - 1, j) + 1,
                    cell(i, j - 1) + 1, cell(i - 1, j - 1) + cost });
                if (i > 1 && j > 1 && a[i - 1] == b[j - 2] && a[i - 2] == b[j - 1])
                {
                    value = (std::min)(value, cell(i - 2, j - 2) + 1);
                }
                cell(i, j) = value;
            }
        }
        return cell(la, lb);
    }

    [[nodiscard]] int Hamming(std::wstring_view a, std::wstring_view b) noexcept
    {
        int distance{};
        for (std::size_t index{}; index < a.size(); ++index)
        {
            if (a[index] != b[index]) ++distance;
        }
        return distance;
    }

    [[nodiscard]] double Jaro(std::wstring_view a, std::wstring_view b)
    {
        if (a.empty() && b.empty()) return 1.0;
        if (a.empty() || b.empty()) return 0.0;

        struct OrderedPositions
        {
            std::vector<int> values;
            std::vector<std::size_t> successor;

            [[nodiscard]] std::size_t Find(std::size_t index) noexcept
            {
                auto root = index;
                while (successor[root] != root) root = successor[root];
                while (successor[index] != index)
                {
                    auto const next = successor[index];
                    successor[index] = root;
                    index = next;
                }
                return root;
            }

            void Remove(std::size_t index) noexcept
            {
                successor[index] = Find(index + 1);
            }
        };

        auto matchDistance = static_cast<int>((std::max)(a.size(), b.size()) / 2) - 1;
        if (matchDistance < 0) matchDistance = 0;
        std::vector<bool> aMatched(a.size());
        std::vector<bool> bMatched(b.size());

        // The managed oracle greedily chooses the first still-unmatched equal
        // code unit in each window. Per-code-unit ordered positions plus a
        // successor set reproduce that exact choice without rescanning a
        // potentially O(n)-wide window for every character.
        std::unordered_map<wchar_t, OrderedPositions> positions;
        positions.reserve((std::min)(b.size(), std::size_t{ 65'536 }));
        for (std::size_t index{}; index < b.size(); ++index)
        {
            positions[b[index]].values.push_back(static_cast<int>(index));
        }
        for (auto& [character, bucket] : positions)
        {
            (void)character;
            bucket.successor.resize(bucket.values.size() + 1);
            for (std::size_t index{}; index < bucket.successor.size(); ++index)
            {
                bucket.successor[index] = index;
            }
        }

        int matches{};
        for (std::size_t i{}; i < a.size(); ++i)
        {
            auto const start = (std::max)(0, static_cast<int>(i) - matchDistance);
            auto const end = (std::min)(static_cast<int>(b.size()),
                static_cast<int>(i) + matchDistance + 1);
            auto bucket = positions.find(a[i]);
            if (bucket == positions.end()) continue;
            auto& ordered = bucket->second;
            auto const lower = std::lower_bound(ordered.values.begin(), ordered.values.end(), start);
            auto positionIndex = ordered.Find(
                static_cast<std::size_t>(lower - ordered.values.begin()));
            if (positionIndex == ordered.values.size() || ordered.values[positionIndex] >= end) continue;
            auto const position = static_cast<std::size_t>(ordered.values[positionIndex]);
            ordered.Remove(positionIndex);
            aMatched[i] = true;
            bMatched[position] = true;
            ++matches;
        }
        if (matches == 0) return 0.0;
        double transpositions{};
        std::size_t k{};
        for (std::size_t i{}; i < a.size(); ++i)
        {
            if (!aMatched[i]) continue;
            while (!bMatched[k]) ++k;
            if (a[i] != b[k]) ++transpositions;
            ++k;
        }
        transpositions /= 2.0;
        auto const m = static_cast<double>(matches);
        return (m / static_cast<double>(a.size()) +
            m / static_cast<double>(b.size()) +
            (m - transpositions) / m) / 3.0;
    }

    [[nodiscard]] double JaroWinkler(std::wstring_view a, std::wstring_view b)
    {
        auto const jaro = Jaro(a, b);
        int prefix{};
        auto const maximum = (std::min)(std::size_t{ 4 }, (std::min)(a.size(), b.size()));
        for (std::size_t index{}; index < maximum && a[index] == b[index]; ++index) ++prefix;
        return jaro + static_cast<double>(prefix) * 0.1 * (1.0 - jaro);
    }

    [[nodiscard]] int LongestCommonSubstring(std::wstring_view a, std::wstring_view b)
    {
        if (a.empty() || b.empty()) return 0;
        std::vector<int> previous(b.size() + 1);
        std::vector<int> current(b.size() + 1);
        int best{};
        for (std::size_t i = 1; i <= a.size(); ++i)
        {
            for (std::size_t j = 1; j <= b.size(); ++j)
            {
                if (a[i - 1] == b[j - 1])
                {
                    current[j] = previous[j - 1] + 1;
                    best = (std::max)(best, current[j]);
                }
                else current[j] = 0;
            }
            std::swap(previous, current);
            std::fill(current.begin(), current.end(), 0);
        }
        return best;
    }

    [[nodiscard]] int LongestCommonSubsequence(std::wstring_view a, std::wstring_view b)
    {
        if (a.empty() || b.empty()) return 0;
        std::vector<int> previous(b.size() + 1);
        std::vector<int> current(b.size() + 1);
        for (std::size_t i = 1; i <= a.size(); ++i)
        {
            for (std::size_t j = 1; j <= b.size(); ++j)
            {
                current[j] = a[i - 1] == b[j - 1]
                    ? previous[j - 1] + 1
                    : (std::max)(previous[j], current[j - 1]);
            }
            std::swap(previous, current);
        }
        return previous[b.size()];
    }
